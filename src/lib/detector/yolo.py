class YoloResult(object):
    def __init__(self, category, confidence, left_top, right_bottom):
        self.category = category
        self.confidence = confidence
        self.left_top = left_top
        self.right_bottom = right_bottom

    def __repr__(self):
        return (f"YoloResult(category: {self.category}, confidence: {self.confidence}, )"
                f"left_top: {self.left_top}, right_bottom: {self.right_bottom}")


